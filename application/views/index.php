<script>
showProductDetails = function(x) {
    $.ajax({
        url: '<?php echo base_url('showProductDetails') ?>',
        method: 'POST',
        data: 'id=' + x,

        success: function(data) {
            $('#details').html(data)
        }
    })
}

function addToCart(x) {

    var details = {
        productid: x.id,
        qty: 1
    };

    $.ajax({
        type: "POST",
        url: '<?php echo base_url('welcome/addToCart') ?>',
        data: details,

        success: function(response) {

            if (response == 0) {

                Swal.fire({
                    icon: 'error',
                    title: 'Oops...',
                    text: 'Something went wrong. Try again.'
                });

            } else if (response == 1) {

                let currentCart = Number($("#cart").text());
                $("#cart").text(currentCart + 1);

                Swal.fire({
                    icon: 'success',
                    title: 'Success',
                    text: 'Item added to Cart.',
                    timer: 1500,
                    showConfirmButton: false
                }).then(() => {
                    location.reload(); // ✅ Refresh page
                });
            }
        },

        error: function() {
            Swal.fire({
                icon: 'error',
                title: 'Server Error',
                text: 'Please try again later.'
            });
        }
    });
}

function showCategoryWiseProduct(categoryId) {
    $.ajax({
        url: '<?= base_url('welcome/getCategoryWiseProducts') ?>',
        type: 'POST',
        data: {
            category_id: categoryId
        },
        success: function(data) {
            $('#productList').html(data);
        },
        error: function() {
            Swal.fire('Error', 'Failed to load products.', 'error');
        }
    });
}

showCombo = function(x) {
    $.ajax({
        type: "POST",
        url: '<?php echo base_url('welcome/getCombo') ?>',
        data: {
            product_id: x
        },
        success: function(response) {
            $('#combo').html(response);
        }
    });
}

